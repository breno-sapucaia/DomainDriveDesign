package descanso8;

public class Cor {
	public String nome;
	public boolean corMetalica;
	
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	public boolean isCorMetalica() {
		return corMetalica;
	}
	public void setCorMetalica(boolean corMetalica) {
		this.corMetalica = corMetalica;
	}
	public String getNome() {
		return this.nome;
	}
	
	
	
}
